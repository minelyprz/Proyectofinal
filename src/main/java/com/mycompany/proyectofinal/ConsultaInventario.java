     private void llenarTabla() {
        modeloTabla.setRowCount(0);
        for (Libros libros : Proyectofinal.libros) {
            modeloTabla.addRow(new Object[]{
                libros.getTitulo(),
                libros.getAutor(),
                libros.getGenero(),
                libros.getPrecio(),
                libros.getCantidad()
            });
        }
    }
     public void actualizarTabla() {
    llenarTabla();
}
 
     
